#Prompt user for radius + print diameter, circumference, area of that circle
radius = float(input("Enter radius:"))

#formula for diameter, circumference, area of circle
diameter = 2*radius
print (f"Diameter {diameter}")
circumference = 2*3.14159*radius
print(f"Circumference {circumference}")
area = 3.14159*(radius)**2
print(f"Area {area}")
